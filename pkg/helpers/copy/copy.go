package copy

// Float32 -  Returns pointer to a float32 value
func Float32(val float32) *float32 {
	return &val
}

// Float64 - Returns pointer to a float64 value
func Float64(val float64) *float64 {
	return &val
}

// Int - Returns pointer to a int value
func Int(val int) *int {
	return &val
}

// Int64 - Returns pointer to a int64 value
func Int64(val int64) *int64 {
	return &val
}

// String - Returns pointer to a string value
func String(val string) *string {
	return &val
}
